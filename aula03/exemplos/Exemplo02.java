package exemplos;

public class Exemplo02 {

    public static void main(String[] args) {
        // TIPO NOME
        int numero; // cria a variavel chamada numero, que guardda um valor inteiro


        numero = 12; // (atribuição) guarda o valor 12 dentro da variavel
        
        System.out.println(numero); // mostre na tela o que tem na variavel

        numero = 2 + 3 * 5; // substitui o conteúdo da variável pelo resultado da expressão

        System.out.println(numero);

     
    }
    
}
