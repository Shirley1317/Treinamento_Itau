public class Exemplo02 {
public static void main(String[] args) {

    // printf( FORMATO, DADOS)
    // FORMATO:
    // %d = inteiro
    // %f = decimal
    // %s = texto
    // \n = pula linha

    // %.2f = 2 casas decimais com arredondamento

    System.out.printf("o número é %d\n", 10);

    System.out.printf("os números são %d e %d\n", 10, 15);

    System.out.printf("a média é %f\n", 45.6754321);

    System.out.printf("a média é %2f\n", 45.6754321);
    
}
    
}
